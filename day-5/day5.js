//107. Looping through arrays using `for` and `for...of`

const fruits = ["apple", "banana", "orange"];
for (let i = 0; i < fruits.length; i++) {
  console.log(fruits[i]);
}

console.log("using for...of loop: ");
for (const x of fruits) {
  console.log(x);
}

